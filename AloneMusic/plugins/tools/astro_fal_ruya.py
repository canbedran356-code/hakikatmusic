# ===== RÜYA =====
@app.on_message(filters.command("ruya"))
async def ruya(client, message: Message):

    if len(message.command) < 2:
        await message.reply("Rüyanı yaz.\nÖrnek:\n`/ruya rüyamda yılan gördüm`")
        return

    text = " ".join(message.command[1:]).lower()
    bulunan = []

    for kelime, yorum in ruya_sozluk.items():
        if kelime in text:
            bulunan.append(f"🔎 **{kelime}** → {yorum}")

    if bulunan:
        await message.reply("🌙 **Rüya Analizi**\n\n" + "\n".join(bulunan))
    else:
        await message.reply("🔮 Rüyada belirgin sembol algılanamadı.")

