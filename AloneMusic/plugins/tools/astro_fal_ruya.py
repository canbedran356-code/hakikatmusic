    if len(message.command) < 2:
        await message.reply("Burcunu yaz.\nÖrnek:\n`/burc aslan`")
        return

    burc = message.command[1].lower()

    if burc in burclar:
        await message.reply(f"♈ **{burc.capitalize()} Burcu**\n\n{burclar[burc]}")
    else:
        await message.reply("Geçerli bir burç yaz.")

